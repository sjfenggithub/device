package com.yd.model;

import java.util.List;

public class CtrlBasicInfo {
	VirtualSwt virtualSwt;
	PhysicalSwt physicalSwt;
	List<Gateway> Gateway;
	List<FW> FW;
	public VirtualSwt getVirtualSwt() {
		return virtualSwt;
	}
	public void setVirtualSwt(VirtualSwt virtualSwt) {
		this.virtualSwt = virtualSwt;
	}
	public PhysicalSwt getPhysicalSwt() {
		return physicalSwt;
	}
	public void setPhysicalSwt(PhysicalSwt physicalSwt) {
		this.physicalSwt = physicalSwt;
	}
	public List<Gateway> getGateway() {
		return Gateway;
	}
	public void setGateway(List<Gateway> gateway) {
		this.Gateway = gateway;
	}
	public List<FW> getFw() {
		return FW;
	}
	public void setFw(List<FW> fw) {
		this.FW = fw;
	}
	public CtrlBasicInfo(VirtualSwt virtualSwt, PhysicalSwt physicalSwt,
			List<Gateway> gateway, List<FW> fw) {
		
		this.virtualSwt = virtualSwt;
		this.physicalSwt = physicalSwt;
		this.Gateway = gateway;
		this.FW = fw;
	}
	
	
}




